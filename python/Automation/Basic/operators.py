# Python Operators

# Arithmetic
# +
print(52+46)
# -
print(78-13)
# *
print(5*5)
# /
print(9/3)
# **
print(5 ** 2)
# %
print(10 % 3)

# Relational
# =
a = 1
print(a)
# ==
print(a == 1)
print(a == 2)
# !=
print(a != 2)
print(a != 1)
# <
print(a < 2)
# >
print(a > 2)
# >=
print(a >= 2)
# <=
print(a <= 2)

# Boolean
# False
print(False)
# True
print(True)
# Not
print(not False)
print(not True)
# Is

# Logic
# and
print(1 == 5 and 2 == 2)
# or
print(1 == 5 or 2 == 2)
